package xyz.kimherala.smolngin.game;

import xyz.kimherala.smolngin.graphics.*;
import xyz.kimherala.smolngin.gui.ComponentTree;
import xyz.kimherala.smolngin.gui.ContainerUIComponent;

import java.util.ArrayList;
import java.util.List;

public class Game implements ApplicationInterface {
    private Window window;
    private List<Entity> cubes;
    private Scene scene;
    private float rotation;

    public Game(Window window) {
        this.window = window;

        float[] positions = new float[]{
                // V0
                -0.5f, 0.5f, 0.5f,
                // V1
                -0.5f, -0.5f, 0.5f,
                // V2
                0.5f, -0.5f, 0.5f,
                // V3
                0.5f, 0.5f, 0.5f,
                // V4
                -0.5f, 0.5f, -0.5f,
                // V5
                0.5f, 0.5f, -0.5f,
                // V6
                -0.5f, -0.5f, -0.5f,
                // V7
                0.5f, -0.5f, -0.5f,

                // For text coords in top face
                // V8: V4 repeated
                -0.5f, 0.5f, -0.5f,
                // V9: V5 repeated
                0.5f, 0.5f, -0.5f,
                // V10: V0 repeated
                -0.5f, 0.5f, 0.5f,
                // V11: V3 repeated
                0.5f, 0.5f, 0.5f,

                // For text coords in right face
                // V12: V3 repeated
                0.5f, 0.5f, 0.5f,
                // V13: V2 repeated
                0.5f, -0.5f, 0.5f,

                // For text coords in left face
                // V14: V0 repeated
                -0.5f, 0.5f, 0.5f,
                // V15: V1 repeated
                -0.5f, -0.5f, 0.5f,

                // For text coords in bottom face
                // V16: V6 repeated
                -0.5f, -0.5f, -0.5f,
                // V17: V7 repeated
                0.5f, -0.5f, -0.5f,
                // V18: V1 repeated
                -0.5f, -0.5f, 0.5f,
                // V19: V2 repeated
                0.5f, -0.5f, 0.5f,
        };
        float[] textCoords = new float[]{
                0.0f, 0.0f,
                0.0f, 0.5f,
                0.5f, 0.5f,
                0.5f, 0.0f,

                0.0f, 0.0f,
                0.5f, 0.0f,
                0.0f, 0.5f,
                0.5f, 0.5f,

                // For text coords in top face
                0.0f, 0.5f,
                0.5f, 0.5f,
                0.0f, 1.0f,
                0.5f, 1.0f,

                // For text coords in right face
                0.0f, 0.0f,
                0.0f, 0.5f,

                // For text coords in left face
                0.5f, 0.0f,
                0.5f, 0.5f,

                // For text coords in bottom face
                0.5f, 0.0f,
                1.0f, 0.0f,
                0.5f, 0.5f,
                1.0f, 0.5f,
        };
        int[] indices = new int[]{
                // Front face
                0, 1, 3, 3, 1, 2,
                // Top Face
                8, 10, 11, 9, 8, 11,
                // Right face
                12, 13, 7, 5, 12, 7,
                // Left face
                14, 15, 6, 4, 14, 6,
                // Bottom face
                16, 18, 19, 17, 16, 19,
                // Back face
                4, 6, 7, 5, 4, 7,};

        scene = new Scene(window.getWidth(), window.getHeight());

        Texture texture = scene.getTextureCache().createTexture(null);
        Material material = new Material();

        material.setTexturePath(texture.getTexturePath());
        List<Material> materialList = new ArrayList<>();
        materialList.add(material);

        Mesh mesh = new Mesh(positions, textCoords, indices);
        material.getMeshList().add(mesh);

        Model cubeModel = new Model("cube-model", materialList);

        scene.addModel(cubeModel);

        cubes = new ArrayList<>();

        for (int i = 0; i < 1; i++) {
            Entity cube = new Entity("cube-entity-" + i, cubeModel.getId());
            cube.setPosition(0, 0, -5);
            scene.addEntity(cube);
            cubes.add(cube);
        }

        ComponentTree<?> gui = new ComponentTree<>(new ContainerUIComponent(0, 0, 1280, 720));
    }

    public Scene getScene() {
        return scene;
    }

    public void update() {
        rotation += 1.5f;
        if ( rotation > 360 ) {
            rotation = 0;
        }

        for (Entity cube : cubes) {
            cube.setRotation(1, 1, 1, (float) Math.toRadians(rotation));
            cube.updateModelMatrix();
        }
    }

    public void cleanup() {
        scene.cleanup();
    }
}
